void mouse_callback(GLFWwindow* window, double xpos, double ypos){
    if(firstMouse){
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }
    float dx = xpos-lastX;
    float dy = lastY - ypos;
    lastX = xpos;
    lastY = ypos;

    float sensitivity = 0.05f;

    camera.ProcessMouseMovement(dx, dy);
}

